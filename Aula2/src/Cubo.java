
public class Cubo extends Quadrado{
	public double largura;
	
	public Cubo(double base, double altura, double largura) {
		super(base, altura);
		this.largura = largura;
	}
	
	
	@Override
	public double area() {
		return super.area();
	} 
	
	
	@Override
	public double calcularDiagonal() {
		return super.calcularDiagonal();
	}
}
