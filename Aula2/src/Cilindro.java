
public class Cilindro extends Circulo{
	
	public double altura;
	
	public Cilindro(double area, double altura, double raio) {
		super(area, raio);
		this.altura = altura;
	}
	@Override
	public double area() {
		return super.area();
	}
	
	
	
	@Override
	public double perimetro() {
		return super.perimetro();
	}
	
}
